# -*- coding: utf8 -*-

a = int(input('Введите первое число: '))
b = int(input('Введите второе число: '))
print('{0} + {1} = '.format(a, b), a+b )
print('{0} - {1} = '.format(a, b), a-b )
print('{0} * {1} = '.format(a, b), a*b )
print('{0} is {1} = '.format(a, b), a is b )
print('{0} == {1} = '.format(a, b), a == b )
print('{0} / {1} = '.format(a, b), a/b )
print('{0} // {1} = '.format(a, b), a//b )
print('{0} > {1} = '.format(a, b), a>b )
print('{0} < {1} = '.format(a, b), a<b )
